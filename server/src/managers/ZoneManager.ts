import { Client } from "@colyseus/core";
import { WorldRoom } from "../rooms/WorldRoom";
import { IZone } from "../rooms/zones/IZone";
import { BeachZone } from "../rooms/zones/BeachZone";
import { VillageZone } from "../rooms/zones/VillageZone";
import { VillageLabZone } from "../rooms/zones/VillageLabZone";
import { Villagehouse1 } from "../rooms/zones/Villagehouse1";
import { Villageflorist } from "../rooms/zones/Villageflorist";
import { Player } from "../schema/PokeWorldState";

// ‚úÖ AJOUT DES IMPORTS POUR LES INTERACTIONS
import { QuestManager } from "./QuestManager";
import { QuestProgressEvent } from "../types/QuestTypes";

export class ZoneManager {
  private zones = new Map<string, IZone>();
  private room: WorldRoom;
  
  // ‚úÖ AJOUT DU QUEST MANAGER
  private questManager: QuestManager;

  constructor(room: WorldRoom) {
    this.room = room;
    console.log(`üó∫Ô∏è === ZONE MANAGER INIT ===`);
    
    // ‚úÖ INITIALISER LE QUEST MANAGER
    this.initializeQuestManager();
    
    this.loadAllZones();
  }

  // ‚úÖ M√âTHODE SIMPLIFI√âE : Initialiser le quest manager
  private initializeQuestManager() {
    try {
      this.questManager = new QuestManager(`../data/quests/quests.json`);
      console.log(`‚úÖ QuestManager initialis√©`);
    } catch (error) {
      console.error(`‚ùå Erreur initialisation QuestManager:`, error);
    }
  }

  private loadAllZones() {
    console.log(`üèóÔ∏è Chargement des zones...`);

    this.loadZone('beach', new BeachZone(this.room));
    this.loadZone('village', new VillageZone(this.room));
    this.loadZone('villagelab', new VillageLabZone(this.room));
    this.loadZone('villagehouse1', new Villagehouse1(this.room));
    this.loadZone('villageflorist', new Villageflorist(this.room));

    console.log(`‚úÖ ${this.zones.size} zones charg√©es:`, Array.from(this.zones.keys()));
  }

  private loadZone(zoneName: string, zone: IZone) {
    console.log(`üì¶ Chargement zone: ${zoneName}`);
    this.zones.set(zoneName, zone);
    console.log(`‚úÖ Zone ${zoneName} charg√©e`);
  }

  async handleZoneTransition(client: Client, data: any) {
    console.log(`üåÄ === ZONE TRANSITION HANDLER ===`);
    console.log(`üë§ Client: ${client.sessionId}`);
    console.log(`üìç Data:`, data);

    const player = this.room.state.players.get(client.sessionId) as Player;
    if (!player) {
      console.error(`‚ùå Player not found: ${client.sessionId}`);
      client.send("transitionResult", { success: false, reason: "Player not found" });
      return;
    }

    const fromZone = player.currentZone;
    const toZone = data.targetZone;

    console.log(`üîÑ Transition: ${fromZone} ‚Üí ${toZone}`);

    const targetZone = this.zones.get(toZone);
    if (!targetZone) {
      console.error(`‚ùå Zone de destination inconnue: ${toZone}`);
      client.send("transitionResult", { success: false, reason: "Zone not found" });
      return;
    }

    try {
      if (fromZone && fromZone !== toZone) {
        console.log(`üì§ Sortie de zone: ${fromZone}`);
        this.onPlayerLeaveZone(client, fromZone);
      }

      player.currentZone = toZone;
      player.map = toZone;
      if (data.spawnX !== undefined) player.x = data.spawnX;
      if (data.spawnY !== undefined) player.y = data.spawnY;

      console.log(`üìç Position mise √† jour: (${player.x}, ${player.y}) dans ${toZone}`);

      console.log(`üì• Entr√©e dans zone: ${toZone}`);
      await this.onPlayerJoinZone(client, toZone);

      client.send("transitionResult", { 
        success: true, 
        currentZone: toZone,
        position: { x: player.x, y: player.y }
      });

      console.log(`‚úÖ Transition r√©ussie: ${player.name} est maintenant dans ${toZone}`);

    } catch (error) {
      console.error(`‚ùå Erreur lors de la transition:`, error);
      client.send("transitionResult", { success: false, reason: "Transition failed" });
    }
  }

  async onPlayerJoinZone(client: Client, zoneName: string) {
    console.log(`üì• === PLAYER JOIN ZONE ===`);
    console.log(`üë§ Client: ${client.sessionId}`);
    console.log(`üåç Zone: ${zoneName}`);

    const zone = this.zones.get(zoneName);
    if (zone) {
      await zone.onPlayerEnter(client);
      await this.room.onPlayerJoinZone(client, zoneName);
      
      // ‚úÖ NOUVEAU: Envoyer les statuts de qu√™tes pour les NPCs de cette zone
      await this.sendQuestStatusesForZone(client, zoneName);
      
      console.log(`‚úÖ Player entered zone: ${zoneName}`);
    } else {
      console.error(`‚ùå Zone not found: ${zoneName}`);
    }
  }

  onPlayerLeaveZone(client: Client, zoneName: string) {
    console.log(`üì§ === PLAYER LEAVE ZONE ===`);
    console.log(`üë§ Client: ${client.sessionId}`);
    console.log(`üåç Zone: ${zoneName}`);

    const zone = this.zones.get(zoneName);
    if (zone) {
      zone.onPlayerLeave(client);
      console.log(`‚úÖ Player left zone: ${zoneName}`);
    } else {
      console.error(`‚ùå Zone not found: ${zoneName}`);
    }
  }

  // ‚úÖ GESTION DES INTERACTIONS NPC AVEC LOGIQUE DE QU√äTES
  async handleNpcInteraction(client: Client, npcId: number) {
    console.log(`üí¨ === NPC INTERACTION HANDLER ===`);
    
    const player = this.room.state.players.get(client.sessionId) as Player;
    if (!player) {
      console.error(`‚ùå Player not found: ${client.sessionId}`);
      client.send("npcInteractionResult", {
        type: "error",
        message: "Joueur non trouv√©"
      });
      return;
    }

    const npcManager = this.room.getNpcManager(player.currentZone);
    if (!npcManager) {
      console.error(`‚ùå NPCManager not found for zone: ${player.currentZone}`);
      client.send("npcInteractionResult", {
        type: "error",
        message: "NPCs non disponibles dans cette zone"
      });
      return;
    }

    const npc = npcManager.getNpcById(npcId);
    if (!npc) {
      console.error(`‚ùå NPC not found: ${npcId}`);
      client.send("npcInteractionResult", {
        type: "error",
        message: "NPC introuvable"
      });
      return;
    }

    console.log(`üí¨ Interaction avec NPC: ${npc.name} dans ${player.currentZone}`);

    try {
      // ‚úÖ 1. V√âRIFIER LE STATUT DES QU√äTES DE CE NPC
      const questStatus = await this.getQuestStatusForNpc(player.name, npc);
      
      switch (questStatus.type) {
        case 'questAvailable':
          // ‚úÖ Qu√™te disponible √† prendre
          client.send("npcInteractionResult", {
            type: "questGiver",
            availableQuests: questStatus.quests,
            npcId: npcId,
            npcName: npc.name
          });
          break;
          
        case 'questReadyToComplete':
          // ‚úÖ Qu√™te pr√™te √† rendre
          client.send("npcInteractionResult", {
            type: "questComplete", 
            questId: questStatus.questId,
            npcId: npcId,
            npcName: npc.name,
            message: `F√©licitations ! Vous avez termin√© la qu√™te !`
          });
          break;
          
        case 'questInProgress':
          // ‚úÖ Qu√™te en cours - dialogue normal
          const progressDialogue = this.getProgressDialogueForNpc(npc, questStatus.quest);
          client.send("npcInteractionResult", {
            type: "dialogue",
            lines: progressDialogue,
            npcId: npcId,
            npcName: npc.name
          });
          break;
          
        case 'noQuest':
        default:
          // ‚úÖ Pas de qu√™te - dialogue normal
          const dialogueLines = this.getDialogueForNpc(npc);
          client.send("npcInteractionResult", {
            type: "dialogue",
            lines: dialogueLines,
            npcId: npcId,
            npcName: npc.name
          });
          break;
      }
      
    } catch (error) {
      console.error(`‚ùå Erreur interaction NPC ${npcId}:`, error);
      client.send("npcInteractionResult", {
        type: "error",
        message: "Erreur lors de l'interaction avec le NPC"
      });
    }
  }

  // ‚úÖ M√âTHODE OPTIMIS√âE: Analyser le statut des qu√™tes pour un NPC
  private async getQuestStatusForNpc(username: string, npc: any) {
    if (!npc.properties?.questId) {
      return { type: 'noQuest' };
    }

    const questId = npc.properties.questId;
    
    // ‚úÖ UTILISER LE QUESTMANAGER EXISTANT - plus efficace !
    const availableQuests = await this.questManager.getAvailableQuests(username);
    const availableQuest = availableQuests.find(q => q.id === questId);
    
    if (availableQuest) {
      return { type: 'questAvailable', quests: [availableQuest] };
    }

    // ‚úÖ V√©rifier les qu√™tes actives
    const activeQuests = await this.questManager.getActiveQuests(username);
    const activeQuest = activeQuests.find(q => q.id === questId);
    
    if (activeQuest) {
      // V√©rifier si pr√™te √† compl√©ter
      if (this.isQuestReadyToComplete(activeQuest)) {
        return { type: 'questReadyToComplete', questId, quest: activeQuest };
      } else {
        return { type: 'questInProgress', quest: activeQuest };
      }
    }

    // ‚úÖ Pas de qu√™te pour ce NPC
    return { type: 'noQuest' };
  }

  // ‚úÖ M√âTHODE OPTIMIS√âE: V√©rifier si une qu√™te est pr√™te √† compl√©ter
  private isQuestReadyToComplete(quest: any): boolean {
    const currentStep = quest.steps[quest.currentStepIndex];
    if (!currentStep) return false;

    // ‚úÖ V√©rifier que tous les objectifs de l'√©tape courante sont compl√©t√©s
    return currentStep.objectives.every((obj: any) => obj.completed);
  }

  // ‚úÖ M√âTHODE HELPER : R√©cup√©rer le dialogue d'un NPC
  private getDialogueForNpc(npc: any): string[] {
    // TODO: Impl√©menter la r√©cup√©ration depuis dialogueId
    if (npc.properties?.dialogueId) {
      // Pour l'instant, dialogue par d√©faut
      switch (npc.properties.dialogueId) {
        case 'greeting_bob':
          return ["Salut ! Je suis Bob, le p√™cheur local.", "J'esp√®re que tu aimes la p√™che !"];
        default:
          return [`Bonjour ! Je suis ${npc.name}.`];
      }
    }
    
    return [`Bonjour ! Je suis ${npc.name}.`];
  }

  // ‚úÖ NOUVELLE M√âTHODE: Dialogue sp√©cifique pendant une qu√™te
  private getProgressDialogueForNpc(npc: any, quest: any): string[] {
    // Dialogues sp√©cifiques selon la qu√™te en cours
    if (quest.id === 'quest_fishingrod') {
      return [
        "Comment va votre recherche de mat√©riel de p√™che ?",
        "J'ai vraiment h√¢te de retourner p√™cher !"
      ];
    }
    
    // Dialogue g√©n√©rique pour qu√™te en cours
    return [
      `Comment avance votre mission ?`,
      `Revenez me voir quand vous aurez termin√© !`
    ];
  }

  // ‚úÖ NOUVELLE M√âTHODE: Envoyer les statuts de qu√™tes
  private async sendQuestStatusesForZone(client: Client, zoneName: string) {
    const player = this.room.state.players.get(client.sessionId) as Player;
    if (!player) return;

    const npcManager = this.room.getNpcManager(zoneName);
    if (!npcManager) return;

    const npcs = npcManager.getAllNpcs();
    const questStatuses = [];

    for (const npc of npcs) {
      if (npc.properties?.questId) {
        const status = await this.getQuestStatusForNpc(player.name, npc);
        questStatuses.push({
          npcId: npc.id,
          type: status.type
        });
      }
    }

    if (questStatuses.length > 0) {
      client.send("questStatuses", { questStatuses });
    }
  }

  // ‚úÖ === M√âTHODES DE D√âL√âGATION AU QUEST MANAGER ===
  // Ces m√©thodes sont des proxies vers le QuestManager

  async handleQuestStart(client: Client, questId: string): Promise<{ success: boolean; message: string; quest?: any }> {
    console.log(`üéØ === QUEST START HANDLER ===`);
    console.log(`üë§ Client: ${client.sessionId}`);
    console.log(`üìú Quest ID: ${questId}`);
    
    const player = this.room.state.players.get(client.sessionId) as Player;
    if (!player) {
      console.error(`‚ùå Player not found: ${client.sessionId}`);
      return {
        success: false,
        message: "Joueur non trouv√©"
      };
    }

    try {
      const quest = await this.questManager.startQuest(player.name, questId);
      
      if (quest) {
        // ‚úÖ NOUVEAU: Mettre √† jour les indicateurs de qu√™te apr√®s d√©marrage
        await this.sendQuestStatusesForZone(client, player.currentZone);
        
        // Broadcaster aux autres joueurs
        this.broadcastToZone(player.currentZone, "questUpdate", {
          player: player.name,
          action: "started",
          questId: questId
        });
        
        console.log(`‚úÖ Qu√™te ${questId} d√©marr√©e pour ${player.name}`);
        
        return {
          success: true,
          quest: quest,
          message: `Qu√™te "${quest.name}" d√©marr√©e !`
        };
      } else {
        return {
          success: false,
          message: "Impossible de d√©marrer cette qu√™te"
        };
      }
      
    } catch (error) {
      console.error(`‚ùå Erreur d√©marrage qu√™te ${questId}:`, error);
      return {
        success: false,
        message: "Erreur lors du d√©marrage de la qu√™te"
      };
    }
  }

  // ‚úÖ D√âL√âGATION: R√©cup√©rer les qu√™tes actives
  async getActiveQuests(username: string): Promise<any[]> {
    try {
      return await this.questManager.getActiveQuests(username);
    } catch (error) {
      console.error(`‚ùå Erreur getActiveQuests:`, error);
      return [];
    }
  }

  // ‚úÖ D√âL√âGATION: R√©cup√©rer les qu√™tes disponibles
  async getAvailableQuests(username: string): Promise<any[]> {
    try {
      return await this.questManager.getAvailableQuests(username);
    } catch (error) {
      console.error(`‚ùå Erreur getAvailableQuests:`, error);
      return [];
    }
  }

  // ‚úÖ D√âL√âGATION: Mettre √† jour la progression des qu√™tes
  async updateQuestProgress(username: string, event: QuestProgressEvent): Promise<any[]> {
    try {
      return await this.questManager.updateQuestProgress(username, event);
    } catch (error) {
      console.error(`‚ùå Erreur updateQuestProgress:`, error);
      return [];
    }
  }

  // ‚úÖ D√âL√âGATION: R√©cup√©rer les statuts de qu√™te pour un joueur
  async getQuestStatuses(username: string): Promise<any[]> {
    try {
      const availableQuests = await this.questManager.getAvailableQuests(username);
      const activeQuests = await this.questManager.getActiveQuests(username);
      
      const questStatuses: any[] = [];
      
      // Statuts pour les qu√™tes disponibles
      for (const quest of availableQuests) {
        if (quest.startNpcId) {
          questStatuses.push({
            npcId: quest.startNpcId,
            type: 'questAvailable'
          });
        }
      }
      
      // Statuts pour les qu√™tes actives
      for (const quest of activeQuests) {
        // Qu√™te pr√™te √† √™tre rendue
        if (quest.currentStepIndex >= quest.steps.length && quest.endNpcId) {
          questStatuses.push({
            npcId: quest.endNpcId,
            type: 'questReadyToComplete'
          });
        }
        // Qu√™te en cours avec des objectifs
        else if (quest.endNpcId) {
          questStatuses.push({
            npcId: quest.endNpcId,
            type: 'questInProgress'
          });
        }
      }
      
      return questStatuses;
    } catch (error) {
      console.error(`‚ùå Erreur getQuestStatuses:`, error);
      return [];
    }
  }

  // M√©thodes utilitaires
  getPlayersInZone(zoneName: string): Player[] {
    const playersInZone = Array.from(this.room.state.players.values())
      .filter((player: Player) => player.currentZone === zoneName);
    
    console.log(`üìä Players in zone ${zoneName}: ${playersInZone.length}`);
    return playersInZone;
  }

  broadcastToZone(zoneName: string, message: string, data: any) {
    console.log(`üì° Broadcasting to zone ${zoneName}: ${message}`);
    
    const clientsInZone = this.room.clients.filter(client => {
      const player = this.room.state.players.get(client.sessionId) as Player;
      return player && player.currentZone === zoneName;
    });
    
    clientsInZone.forEach(client => {
      client.send(message, data);
    });
    
    console.log(`üì§ Message envoy√© √† ${clientsInZone.length} clients dans ${zoneName}`);
  }
}
